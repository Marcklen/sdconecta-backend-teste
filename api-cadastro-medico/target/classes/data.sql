CREATE TABLE TB_USER (
	ID INTEGER PRIMARY KEY AUTO_INCREMENT,
	EMAIL VARCHAR(255),
	PASSWORD VARCHAR(255),
	NAME VARCHAR(255),
	SURNAME VARCHAR(255),
	MOBILE_PHONE VARCHAR(255)
);

CREATE TABLE TB_CRM (
	ID INTEGER PRIMARY KEY AUTO_INCREMENT,
	CRM VARCHAR(45),
	UF VARCHAR(2),
	SPECIALTY VARCHAR(255),
	USER_ID INTEGER REFERENCES TB_USER (ID)
);